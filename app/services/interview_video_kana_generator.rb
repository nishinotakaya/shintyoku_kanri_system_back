# 台本を「読み上げ最適化版」にする。
# mode: "optimize"(既定) = 漢字を残し誤読しやすい語だけ仮名(抑揚を保つ)
#       "full"           = 全部ひらがな(誤読ゼロ優先。抑揚は声クローンに依存)
class InterviewVideoKanaGenerator
  def initialize(user:, script:, mode: "optimize")
    @user = user
    @script = script.to_s
    @mode = mode == "full" ? "full" : "optimize"
  end

  def call
    return "" if @script.strip.empty?
    api_key = OpenaiClient.api_key_for(@user)
    sys = @mode == "full" ? FULL_SYS : SYS
    OpenaiJson.chat_json(system: sys, user: @script, api_key: api_key, model: "gpt-4o", temperature: 0)["kana"].to_s.strip
  end

  # 全部ひらがな: 誤読を確実にゼロにする(抑揚は声クローンに依存)
  FULL_SYS = <<~SYS.freeze
    あなたは音声合成(TTS)向けの読み仮名変換器です。与えられたテキストを、漢字を一切残さず全てひらがなに変換します。
    次の JSON で返してください: { "kana": "全てひらがなの読み(全文)" }
    【ルール】
    - 漢字は全て正しい読みのひらがなにする(例: 月収→げっしゅう、一人前→いちにんまえ、西野→にしの、行った→おこなった/いった は文脈で判断)。
    - 数字・金額・助数詞も読み下す(例: 70万円→ななじゅうまんえん、3つ→みっつ、13万円→じゅうさんまんえん)。
    - カタカナ語・英語は読み下す(例: SE→えすいー、YouTube→ゆーちゅーぶ、React→りあくと)。
    - 句読点(、。)・「。。。」(間)・改行・【見出し】はそのまま残す。意味や語順は変えない。原文に無い語を足さない。
  SYS

  SYS = <<~SYS.freeze
    あなたは音声合成(TTS)向けの日本語読み上げ原稿の校正者です。
    目的は「正しい読み」と「自然な抑揚」の両立です。全部をひらがなにすると抑揚が平坦になるので絶対にしないでください。
    次の JSON で返してください: { "kana": "読み上げ最適化テキスト(全文)" }
    【方針】
    - 基本は原文の漢字を残す(漢字があるほうがTTSのアクセント・抑揚が自然になるため)。
    - 誤読しやすい語だけ、ひらがなの読みに置き換える。対象は主に:
      ・人名・社名・サービス名などの固有名詞(例: 西野→にしの)
      ・数字＋助数詞や金額(例: 70万円→ななじゅうまんえん、3つ→みっつ)、一人前→いちにんまえ 等の難読語
      ・複数の読みがある難読語・専門用語で誤読が想定されるもの
    - 一般的に正しく読める漢字語(転職・経験・勉強 等)はそのまま漢字で残す。
    - カタカナ語・英語は読み下す(例: SE→エスイー、YouTube→ユーチューブ)。カタカナへの全面変換はしない。
    - 句読点(、。)・「。。。」・改行・【見出し】はそのまま残す(間の取り方を保つ)。意味や語順は変えない。
  SYS
end
